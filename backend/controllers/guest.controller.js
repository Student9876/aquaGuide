import Guest from "../models/guestModel.js";

export const createGuest = async (req, res) => {
  try {
    // If behind proxy (recommended)
    const ipAddress = req.headers["x-forwarded-for"]?.split(",")[0] || req.ip;

    // 1️⃣ Check if guest already exists
    let guest = await Guest.findOne({
      where: { ip_address: ipAddress },
    });

    // 2️⃣ If exists, return it
    if (guest) {
      return res.status(200).json({
        success: true,
        message: "Guest already exists",
        guest,
      });
    }

    // 3️⃣ Create new guest
    guest = await Guest.create({
      ip_address: ipAddress,
      // guest_name will be auto-generated by hook
    });

    return res.status(201).json({
      success: true,
      message: "Guest created successfully",
      guest,
    });
  } catch (error) {
    // 4️⃣ Handle unique constraint race condition
    if (error.name === "SequelizeUniqueConstraintError") {
      const guest = await Guest.findOne({
        where: { ip_address: req.ip },
      });

      return res.status(200).json({
        success: true,
        message: "Guest already exists",
        guest,
      });
    }

    console.error("Guest create error:", error);
    return res.status(500).json({
      success: false,
      message: "Internal server error",
    });
  }
};
